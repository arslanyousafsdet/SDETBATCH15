package class6;

public class IncrementOpertors {
    public static void main(String[] args) {
        int number=10;// for increment of the number
        number=number+1;
        System.out.println(number);
        number+=1;
        System.out.println(number);
        number++;
        System.out.println(number);
        int number2=10;
        number2--;// for decrement of the number
        System.out.println(number2);
    }
}
