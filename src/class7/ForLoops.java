package class7;
import java.util.Scanner;

public class ForLoops {
    public static void main(String[] args) {
        /* prints number from 0 to 9/**/
       int number=0;
        while(number<=9){
            System.out.println(number);
            number++;
        }

 for(int i=0;i<10 ;i++){
     System.out.println(i);
 }

    }
}
